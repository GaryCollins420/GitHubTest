     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $me = Auth::user();
        $invoices = [];

        switch ($me->role) {
            case "customer":
                $invoices = Invoice::where('user_id', $me->id)->get();
                break;
            case "ceo":
            case "administrator":
                $invoices = Invoice::all();
                break;
        }

        return view('invoices.invoice', compact('invoices'));
    }

